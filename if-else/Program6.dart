
void main(){
  int n=2;

  switch(n)
  {
    case 0:
        print("ZERO");
    case 1:
        print("ONE");
    case 2:
        print("TWO");
    case 3:
        print("THREE");
    case 4:
        print("FOUR");
    case 5:
        print("FIVE");

    default:
    print("$n is greater than 5");

  }
}